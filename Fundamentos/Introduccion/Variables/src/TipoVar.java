public class TipoVar {
    public static void main(String[] args) {
        System.out.println("*** Uso de Var en Java ***");
        var nombre1 = "Juan";
        var edad = 30;
        var sueldo = 5000.5F;
        var esCasado = false;

        System.out.println(nombre1);
        System.out.println(edad);
        System.out.println(sueldo);
        System.out.println(esCasado);
    }
}
